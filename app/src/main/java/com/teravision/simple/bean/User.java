package com.teravision.simple.bean;

import java.io.Serializable;
import java.util.Date;

/**
 * Created by Sara Villarreal on 7/27/2016.
 */
public class User implements Serializable {


    private Long entityId;
    private String lastName;
    private String firstName;
    private String gender;
    private String password;
    private String email;
    private String facebook;
    private String google;
    private Date createdDate;
    private Date lastModified;
    private Date lastLoginDate;
    private Date eliminationDate;
    private Boolean passwordRequest;
    private int imageId;
    private String deviceToken;
    private String devicePlatform;

    public Long getEntityId() {
        return entityId;
    }

    public void setEntityId(Long entityId) {
        this.entityId = entityId;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFacebook() {
        return facebook;
    }

    public void setFacebook(String facebook) {
        this.facebook = facebook;
    }

    public String getGoogle() {
        return google;
    }

    public void setGoogle(String google) {
        this.google = google;
    }

    public Date getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(Date createdDate) {
        this.createdDate = createdDate;
    }

    public Date getLastModified() {
        return lastModified;
    }

    public void setLastModified(Date lastModified) {
        this.lastModified = lastModified;
    }

    public Date getLastLoginDate() {
        return lastLoginDate;
    }

    public void setLastLoginDate(Date lastLoginDate) {
        this.lastLoginDate = lastLoginDate;
    }

    public Date getEliminationDate() {
        return eliminationDate;
    }

    public void setEliminationDate(Date eliminationDate) {
        this.eliminationDate = eliminationDate;
    }

    public Boolean getPasswordRequest() {
        return passwordRequest;
    }

    public void setPasswordRequest(Boolean passwordRequest) {
        this.passwordRequest = passwordRequest;
    }

    public int getImageId() {
        return imageId;
    }

    public void setImageId(int imageId) {
        this.imageId = imageId;
    }

    public String getDeviceToken() {
        return deviceToken;
    }

    public void setDeviceToken(String deviceToken) {
        this.deviceToken = deviceToken;
    }

    public String getDevicePlatform() {
        return devicePlatform;
    }

    public void setDevicePlatform(String devicePlatform) {
        this.devicePlatform = devicePlatform;
    }








}
